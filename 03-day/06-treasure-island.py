print("Your mission is to find the treasure.")

direction = input("Where do you want to go? Left or right? ").lower()


if direction == "left":
    action = input("Do you want to swim or do you want to wait? Swim or wait? ").lower()

    if action == "wait":
        doorColor = input("Which door do you want to choose? Red, Blue or Yellow? ").lower()

